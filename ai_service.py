chat_histories: dict[int, list[dict]] = {}
MAX_HISTORY = 20  # последних сообщений храним


def get_history(chat_id: int) -> list[dict]:
    return chat_histories.get(chat_id, [])


def add_to_history(chat_id: int, role: str, content: str):
    if chat_id not in chat_histories:
        chat_histories[chat_id] = []
    
    chat_histories[chat_id].append({
        "role": role,
        "content": content
    })
    
    # Обрезаем историю
    if len(chat_histories[chat_id]) > MAX_HISTORY:
        chat_histories[chat_id] = chat_histories[chat_id][-MAX_HISTORY:]


def clear_history(chat_id: int):
    if chat_id in chat_histories:
        del chat_histories[chat_id]


def _parse_ai_response(data: dict) -> str | None:
    """
    Парсит ответ от AI API, поддерживая разные форматы.
    
    Args:
        data: JSON-ответ от API
        
    Returns:
        Текст ответа или None, если не удалось распарсить
    """
    # Стандартный формат OpenAI-подобных API
    if "choices" in data and data["choices"]:
        choice = data["choices"][0]
        
        # Формат с message (наиболее распространённый)
        if "message" in choice:
            content = choice["message"].get("content")
            if content:
                return content
        
        # Формат с text (альтернативный)
        if "text" in choice:
            return choice["text"]
    
    # Формат с вложенным data
    if "data" in data:
        nested_data = data["data"]
        if "choices" in nested_data and nested_data["choices"]:
            choice = nested_data["choices"][0]
            if "message" in choice:
                return choice["message"].get("content")
    
    # Прямой ответ в content
    if "content" in data:
        return data["content"]
    
    # Прямой ответ в text
    if "text" in data:
        return data["text"]
    
    return None


async def ask_ai(
    chat_id: int,
    user_message: str,
    bot_name: str | None = None,
    extra_context: str = "",
    max_tokens: int = 500,
    temperature: float = 0.85,
) -> str:
    """
    Отправляет сообщение в AI и возвращает ответ.
    
    Args:
        chat_id: ID чата для хранения истории
        user_message: Сообщение от пользователя
        bot_name: Имя бота (по умолчанию "бот")
        extra_context: Дополнительный контекст для системы
        max_tokens: Максимальное количество токенов в ответе
        temperature: Температура генерации (0.0 - 2.0)
        
    Returns:
        Ответ от AI или сообщение об ошибке
        
    Raises:
        ValueError: Если user_message пустой
    """
    # Валидация ввода
    if not user_message or not user_message.strip():
        raise ValueError("Сообщение не может быть пустым")
    
    if chat_id <= 0:
        raise ValueError(f"Некорректный chat_id: {chat_id}")
    
    # Нормализация параметров
    user_message = user_message.strip()
    bot_name = bot_name.strip() if bot_name else "бот"
    
    # Добавляем сообщение пользователя в историю
    add_to_history(chat_id, "user", user_message)
    
    # Формируем system prompt
    system = SYSTEM_PROMPT.format(bot_name=bot_name)
    if extra_context and extra_context.strip():
        system += f"\n\nДополнительный контекст: {extra_context.strip()}"
    
    # Получаем историю и формируем список сообщений
    history = get_history(chat_id)
    messages = [
        {"role": "system", "content": system},
        *history
    ]
    
    # Подготовкаpayload для API
    payload = {
        "model": "minimax/minimax-m2.5:free",
        "messages": messages,
        "max_tokens": max_tokens,
        "temperature": temperature,
        "stream": False
    }
    
    headers = {
        "Authorization": f"Bearer {KILOCODE_API_KEY}",
        "Content-Type": "application/json",
        "Accept": "application/json"
    }
    
    try:
        async with aiohttp.ClientSession() as session:
            async with session.post(
                KILOCODE_URL,
                json=payload,
                headers=headers,
                timeout=aiohttp.ClientTimeout(total=60)
            ) as resp:
                
                # Обработка HTTP ошибок
                if resp.status == 401:
                    print("[AI ERROR] Неверный API ключ")
                    return "Ошибка авторизации. Проверьте API ключ."
                
                if resp.status == 429:
                    print("[AI ERROR] Превышен лимит запросов")
                    return "Слишком много запросов. Попробуйте позже."
                
                if resp.status >= 500:
                    print(f"[AI ERROR] Серверная ошибка: {resp.status}")
                    return "Временная ошибка сервиса. Попробуйте позже."
                
                if resp.status >= 400:
                    error_text = await resp.text()
                    print(f"[AI ERROR] {resp.status}: {error_text[:200]}")
                    return "Произошла ошибка при обработке запроса."
                
                data = await resp.json()
                
                # Парсим ответ с поддержкой разных форматов
                content = _parse_ai_response(data)
                
                if not content:
                    print(f"[AI] Неизвестный формат ответа: {str(data)[:300]}")
                    return "Не удалось обработать ответ. Попробуйте переформулировать запрос."
                
                # Нормализация текста: замена длинных тире на короткие
                content = content.replace("—", "-").replace("–", "-")
                
                # Сохраняем ответ в историю
                add_to_history(chat_id, "assistant", content)
                
                return content
                
    except aiohttp.ClientConnectorError as e:
        print(f"[AI] Ошибка соединения: {e}")
        return "Нет подключения к интернету. Проверьте соединение."
        
    except aiohttp.ServerTimeoutError as e:
        print(f"[AI] Превышен таймаут: {e}")
        return "Запрос занял слишком долго. Попробуйте ещё раз."
        
    except aiohttp.ClientError as e:
        print(f"[AI] Ошибка клиента: {e}")
        return "Произошла ошибка при отправке запроса."
        
    except Exception as e:
        print(f"[AI] Непредвиденная ошибка: {type(e).__name__}: {e}")
        return "Произошла непредвиденная ошибка. Попробуйте позже."